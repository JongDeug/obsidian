// 산술 연산자 (Arithmetic operators)
/**
 * + 더하기
 * - 빼기
 * * 곱하기
 * % 나머지값
 * / 나누기
 * ** 지수 (거듭제곱)
 */

console.log(5 + 2);
console.log(5 - 2);
console.log(5 * 2);
console.log(5 % 2);
console.log(5 / 2);
console.log(5 ** 2); // console.log(Math.pow(5, 2))

let text = '1'+1; // 숫자와 문자열을 더하면 문자열로 변환됨
console.log(text);